char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*tmp;

	i = 0;
	tmp = (char *)s;
	while (s[i])
	{
		if (tmp[i] == c)
			return (&tmp[i]);
		i++;
	}
	if (c == '\0')
		return (&tmp[i]);
	return (0);
}
